package com.api.graphing;

public class BarGraph {
	String y;
	long a;
	long b;
	
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public long getA() {
		return a;
	}
	public void setA(long a) {
		this.a = a;
	}
	public long getB() {
		return b;
	}
	public void setB(long b) {
		this.b = b;
	}
	
	

}
